
import math

def get_oval_surface(rad):
    """Calculates the surface area of a circle."""
    return math.pi * rad * rad

def get_quad_surface(side_a, side_b):
    """Calculates the surface area of a four-sided polygon."""
    return side_a * side_b

def get_slope_surface(base_val, height_val):
    """Calculates the surface area of a triangular shape."""
    return 0.5 * base_val * height_val
  
